package pacote2892028989.MODEL;

public class Ponto {

	private int x, y;

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}

	public void setX(int x) {
		this.x = x;
	}

	public void setY(int y) {
		this.y = y;
	}

	public Ponto(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}
	
	
}
